public class If{

    public static void main(String[] a){

        int numero1 = 8;
        int numero2 = 8;

        if(numero1 <= numero2){
            System.out.printf("%d es menor que %d\n", numero1, numero2);
        }
        if(numero1 >= numero2){
            System.out.printf("%d es mayor que %d\n", numero1, numero2);
        }
        if(numero1 == numero2){
            System.out.printf("%d es igual que %d\n", numero1, numero2);
        }
        if(numero1 != numero2){
            System.out.printf("%d es diferente de %d\n", numero1, numero2);
        }


    }

}
